import { Request, Response } from 'express';
import OpenAI from 'openai';

let openai: OpenAI | null = null;
const getOpenAI = () => {
  if (!openai) openai = new OpenAI({ apiKey: process.env.OPENAI_API_KEY });
  return openai;
};

export const processEntry = async (req: Request, res: Response) => {
  try {
    const { text } = req.body as { text: string };
    if (!text) return res.status(400).json({ error: 'text is required' });

    const today = new Date().toISOString().slice(0, 10);

    const now = new Date().toISOString();

    const completion = await getOpenAI().chat.completions.create({
      model: 'gpt-4o-mini',
      messages: [
        {
          role: 'system',
          content: `Та Монгол хэлний текст задлан шинжлэгч.
Одоогийн цаг: ${now}
Өнөөдрийн огноо: ${today}

Хэрэглэгчийн ярианаас БҮГДИЙГ гарган JSON форматаар буцаа. Өөр текст нэмэхгүй:
{
  "tasks": [{ "title": "даалгаврын нэр", "due": "YYYY-MM-DDTHH:mm:ss эсвэл YYYY-MM-DD эсвэл хоосон", "category": "Ажил|Хувийн|Эрүүл мэнд|Гэр бүл|Сурлага|Хобби|Бусад", "recurring": {"type": "monthly_days|weekly_days", "days": [1,3,5]} }],
  "events": [{ "title": "үйл явдлын нэр", "datetime": "ISO 8601 эсвэл хоосон мөр" }],
  "summary": "нэг өгүүлбэр хураангуй"
}

ДАВТАГДАХ ХУВААРЬ (recurring):
Нэг удаагийн даалгавар бол recurring талбар ороохгүй. Давтагдах тохиолдолд due="" байна.

▸ weekly_days — долоо хоногийн өдрийн нэрс болон тооны нэрс:
  ┌─────────────┬─────────────┬───────┐
  │ Монгол нэр  │ Тооны нэр   │ Утга  │
  ├─────────────┼─────────────┼───────┤
  │ Даваа       │ 1 дэх       │  1    │
  │ Мягмар      │ 2 дох       │  2    │
  │ Лхагва      │ 3 дах       │  3    │
  │ Пүрэв       │ 4 дэх       │  4    │
  │ Баасан      │ 5 дах       │  5    │
  │ Бямба       │ хагасайн    │  6    │
  │ Ням         │ бүтэнсайн   │  0    │
  └─────────────┴─────────────┴───────┘
  ТАНИХ ДҮРЭМ:
  - Долоо хоногийн өдрийн нэр (Даваа/Мягмар/Лхагва/Пүрэв/Баасан/Бямба/Ням) → weekly_days
  - "1 дэх"/"2 дох"/"3 дах"/... → weekly_days (1=Даваа, 2=Мягмар, 3=Лхагва, ...)
  - "бүр"/"болгон" байвал confirmed:true нэм. Байхгүй ч recurring-г нэм.
  - "бүр" ≠ "бүтэнсайн". "бүтэнсайн"=Ням=0. "хагасайн"=Бямба=6.
  Жишээ: "Даваа Лхагва бүр хичээл" → {"type":"weekly_days","days":[1,3],"confirmed":true}
  Жишээ: "Мягмар Пүрэв болгон дасгал" → {"type":"weekly_days","days":[2,4],"confirmed":true}
  Жишээ: "Даваа, Мягмар, Лхагва дасгал" → {"type":"weekly_days","days":[1,2,3]}
  Жишээ: "1 дэх, 3 дах, 5 дах" → {"type":"weekly_days","days":[1,3,5]}
  Жишээ: "1 дэх, 2 дох, 3 дах өдрүүдэд уулзалт" → {"type":"weekly_days","days":[1,2,3]}
  Жишээ: "Даваа бүр дасгал" → {"type":"weekly_days","days":[1],"confirmed":true}
  Жишээ: "Баасан болгон уулзалт" → {"type":"weekly_days","days":[5],"confirmed":true}
  Жишээ: "хагасайн, бүтэнсайн" → {"type":"weekly_days","days":[6,0]}

▸ monthly_days — ЗӨВХӨН "сар бүрийн"/"сар бүр" гэх мэт сарын давталт дурдсан үед:
  АНХААР: "1 дэх", "2 дох" гэх мэт → weekly_days (Даваа, Мягмар), monthly биш!
  Жишээ: "сар бүрийн 1, 15, 20-нд" → {"type":"monthly_days","days":[1,15,20]}
  Жишээ: "сар бүр 10-нд" → {"type":"monthly_days","days":[10]}

АНГИЛАЛ ДҮРМҮҮД (category):
- "Ажил": ажил, хурал, тайлан, имэйл, төсөл, дэд захирал, компани, шефтэй холбоотой
- "Хувийн": хувийн хэрэг, найз, цэнгэл, хобби биш бол
- "Эрүүл мэнд": эмч, эм, тамир, дасгал, унтах, эрүүл, спорт
- "Гэр бүл": гэр, гэр бүл, эцэг эх, хүүхэд, ах дүү
- "Сурлага": сурах, хичээл, шалгалт, ном, курс, их сургууль
- "Хобби": тоглоом, кино, ном унших (таашаалаар), уран бүтээл
- Тодорхойгүй бол "Бусад"

TASK гэж юу вэ — дараах БҮГДИЙГ tasks массивт нэм:
- Хийх, дуусгах, бичих, илгээх, авах, очих, залгах, шалгах зэрэг үйл хийх шаардлагатай аливаа зүйл
- Уулзалт, хурал, ярилцлага, захиалга зэрэг цагтай үйл явдал
- "хэрэгтэй", "ёстой", "болох", "мартаж болохгүй" гэх мэт үгтэй өгүүлбэр
- Тодорхой огноо, цаг дурдсан аливаа зүйл

ОГНОО, ЦАГ ДҮРМҮҮД:
- "маргааш" → өнөөдрөөс +1 өдөр
- "нөгөөдөр" → өнөөдрөөс +2 өдөр
- "дараа өдөр" → өнөөдрөөс +1 өдөр
- "энэ долоо хоногт" → ойрын ажлын өдөр
- Цаг дурдсан бол due-г ЗААВАЛ YYYY-MM-DDTHH:mm:ss форматаар бич (24 цагийн формат)
- Зөвхөн огноо мэдэгдвэл YYYY-MM-DD форматаар бич
- Огноо тодорхойгүй бол due-г хоосон "" үлдээ

ЦАГ ЗАДЛАХ ДҮРМҮҮД (ЧУХАЛ — 24 цагийн формат):
- "X цаг", "X цагт", "X цагийн" → HH:00:00 (24 цагаар)
- "16 цаг" / "16 цагт" → T16:00:00
- "9 цаг" / "9 цагт" → T09:00:00
- "өглөөний X цаг" → X цаг (08:00 орчим бол AM гэж ойлго)
- "оройн X цаг" → X < 8 бол +12 (оройн 8 → T20:00:00)
- "үдийн 12 цаг" → T12:00:00
- "X цаг Y минут" → THH:MM:00
- Жишээ: "маргааш 16 цагт уулзалт" → due: "${today}T16:00:00" (маргааш өдрийн огноо)
- Жишээ: "өнөөдөр оройн 8 цагт" → due: "${today}T20:00:00"
- Жишээ: "9 цагт эмчид" → due: "${today}T09:00:00"`,
        },
        {
          role: 'user',
          content: text,
        },
      ],
      response_format: { type: 'json_object' },
    });

    const raw = completion.choices[0].message.content ?? '{}';
    const parsed = JSON.parse(raw);

    res.json({
      tasks: parsed.tasks ?? [],
      events: parsed.events ?? [],
      summary: parsed.summary ?? '',
    });
  } catch (err) {
    res.status(500).json({ error: (err as Error).message });
  }
};
